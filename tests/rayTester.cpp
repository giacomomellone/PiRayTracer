TEST_GROUP(RaysTest) {};

TEST(RaysTest, raysDistance)
{
    Ray r(Point(2, 3, 4), Vector(1, 0, 0));

    CHECK(Point(2, 3, 4) == r.Position(0));
    CHECK(Point(3, 3, 4) == r.Position(1));
    CHECK(Point(1, 3, 4) == r.Position(-1));
    CHECK(Point(4.5, 3, 4) == r.Position(2.5));
}
