#ifndef LIGHT_HPP
#define LIGHT_HPP

/*******************************************************************************
 *    INCLUDED FILES
 ******************************************************************************/
#include "tuple.h"
#include "color.h"

/*******************************************************************************
 *    PUBLIC TYPES
 ******************************************************************************/

/**
 * Light object
 *
 */
class Light
{
	public:
		Color intensity;
		Tuple position;

		/********** CTOR / DTOR ***********/
		Light()
		{
		    this->intensity = Color(1, 1, 1);
		    this->position = Point(0, 0, 0);
		}

		Light(Tuple pos, Color intensity)
		{
		    this->intensity = intensity;
		    this->position = pos;
		}

		/********** PUBLIC METHOD PROTOTYPES ***********/

};

/*******************************************************************************
 *    CLASS SUPPORT FUNCTIONS PROTOTYPES
 ******************************************************************************/

#endif // LIGHT_HPP
