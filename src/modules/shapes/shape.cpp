int Shape::shapesCount = 0;

Shape::Shape()
{
    this->shapeID = ++this->shapesCount;
}

/*******************************************************************************
 *    PUBLIC METHODS
 ******************************************************************************/
bool operator== (const Shape &s1, const Shape &s2)
{
    if(s1.shapeID == s2.shapeID)
    {
        return true;
    }
    else
        return false;
}

void Shape::SetTranformation(Matrix m)
{
    this->transfM = m;
}
