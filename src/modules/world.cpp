World::World()
{
    /* Define the standard world with the following features */
    this->lightP = Light(Point(-10, 10, -10), Color(1, 1, 1));

    Material matS1(Color(0.8, 1.0, 0.6), 0.1, 0.7, 0.2, 200);
    this->s1 = Sphere(matS1);

    this->s2 = Sphere();
    this->s2.SetTranformation(Scaling(0.5, 0.5, 0.5));
}

